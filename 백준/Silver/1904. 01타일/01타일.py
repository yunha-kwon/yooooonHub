'''
dp[1] = 1                                                       => 1개
dp[2] = 11, 00                                                  => 2개
dp[3] = 111, 100, 001                                           => 3개
dp[4] = 1111, 1100, 1001, 0011, 0000                            => 5개
dp[5] = 11111, 11001, 10011, 00111, 00001, 11100, 10000, 00100  => 8개
=> 직전 거에서 1 붙이고, 하나 더 이전 거에서 0 붙이기
'''

N = int(input())

dp = [0] * 1000001
dp[1] = 1
dp[2] = 2

for i in range(3, N+1):
    dp[i] = (dp[i-1] + dp[i-2]) % 15746

print(dp[N])
